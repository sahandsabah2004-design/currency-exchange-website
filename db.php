$password = "";
$dbname   = "currency_db";

// دروستکردنی پەیوەندی
$conn = new mysqli($host, $username, $password, $dbname);

// پشکنینی پەیوەندی بە شێوازێکی ئارام
if ($conn->connect_error) {
    // لەجیاتی پیشاندانی هەڵە وردەکارییەکان، نامەیەکی گشتی نیشان دەدەین
    // لە پرۆژەی گەورەدا لێرەدا هەڵەکە لە فایلی log پاشەکەوت دەکرێت
    die("Error: ناتوانرێت پەیوەندی بە سیستەمەوە بکرێت. تکایە دواتر هەوڵ بدەرەوە.");
}

// دڵنیابوونەوە لە پشتگیریکردنی زمانی کوردی و عەرەبی بە دروستی
$conn->set_charset("utf8mb4");
